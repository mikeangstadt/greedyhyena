<p>The epsilon greedy strategy is an algorithmic solution to solving <i>the multi-arm bandit</i> problem - whereas hypothesizing that one (or more) of a number of slot machines in a row pay out more frequently than others you seek to identify the 'winningest' machine.  Following the multi-arm bandit slot machine analogy - consider that instead of each machine paying out equally for each winning pull, some machines may pay out at different rates per win.  That is, any given "win" on any given machine may have a varying reward value for the bandit.  In fact, this sophistication more accurately represents the behavior of this fictional system and other important real-world ones.   This has particularly interesting applications in eCommerce where each sale conversion has a definitive value that can be assigned to it's success. </p>
